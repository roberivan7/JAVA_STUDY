package vetores_array;

public class array_caracteres {
    public static void main(String[] args) {
        String curso = "Java";
        System.out.println(curso.charAt(0));
        System.out.println(curso.isEmpty());
        System.out.println(curso.endsWith("av"));
        System.out.println(curso.concat(" muito foda !!!"));
        System.out.println(curso.split(" "));
    }
}
